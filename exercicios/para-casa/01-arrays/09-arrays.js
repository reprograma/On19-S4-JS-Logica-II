const arrayExercicio = [
  "banana",
  "pera",
  "uva",
  "maça",
  "figo",
  "abacaxi",
  "caviar"
];

// inverta a ordem da array com .reverse()
const contrario = arrayExercicio.reverse();
console.log(contrario);

// coloque a array em ordem alfabética
const ordem = arrayExercicio.sort();
console.log(ordem);

// coloque a array em ordem alfabética decrescente z -> a
const desordem = ordem.reverse();
console.log(desordem);